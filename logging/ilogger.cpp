#include "ilogger.hpp"

#include <memory>

#include "fmt-log.hpp"

//////////////////////////////////////////////////////////////////////////
//                                                                      //
//////////////////////////////////////////////////////////////////////////

namespace SQLEngine::Logging
{
    //////////////////////////////////////////////////////////////////////
    //                                                                  //
    //////////////////////////////////////////////////////////////////////

    auto MakeUniqueLogger() -> ULogger
    {
        ULogger logger = std::make_unique<FMTLogger>();
        return logger;
    }

    auto MakeSharedLogger() -> ShLogger
    {
        auto&& unique                     = MakeUniqueLogger();
        ShLogger shared = std::move(unique);
        return shared;
    }

    //////////////////////////////////////////////////////////////////////
    //                                                                  //
    //////////////////////////////////////////////////////////////////////
}  // namespace SQLEngine::Logging

