int SQLQueryLexer::ProcessDigit()
{
    m_currentLexem = "number";
    m_currentValue = yytext;
    return 1;
}

int SQLQueryLexer::ProcessAlpha()
{
    m_currentLexem = "alpha";
    m_currentValue = yytext;
    return 1;
}

//////////////////////////////////////////////////////////////////////

int SQLQueryLexer::ProcessUnknown()
{
    m_currentLexem = "unknown";
    m_currentValue = yytext;
    return 1;
}

void SQLQueryLexer::PrintCurrent() const
{
    out << m_currentLexem << " <" << m_currentValue << ">" << std::endl;
}

